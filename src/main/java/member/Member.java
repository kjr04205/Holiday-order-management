package member;

public class Member {
	private String memberName;
	private int memberPhone;
	private String memberAddress;
	private String memberOrder;
	private int memberMoney;
	private String memberInfo;
	
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public int getMemberPhone() {
		return memberPhone;
	}
	public void setMemberPhone(int memberPhone) {
		this.memberPhone = memberPhone;
	}
	public String getMemberAddress() {
		return memberAddress;
	}
	public void setMemberAddress(String memberAddress) {
		this.memberAddress = memberAddress;
	}
	public String getMemberOrder() {
		return memberOrder;
	}
	public void setMemberOrder(String memberOrder) {
		this.memberOrder = memberOrder;
	}
	public int getMemberMoney() {
		return memberMoney;
	}
	public void setMemberMoney(int memberMoney) {
		this.memberMoney = memberMoney;
	}
	public String getMemberInfo() {
		return memberInfo;
	}
	public void setMemberInfo(String memberInfo) {
		this.memberInfo = memberInfo;
	}
	
	
}
